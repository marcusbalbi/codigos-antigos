    private void mostra_dados() {
        try
        {
            
            tf_codigo.setText(con_cidade.result.getString("codigo"));
            tf_nome.setText(con_cidade.result.getString("nome"));
            cb_uf.setSelectedItem(con_cidade.result.getString("uf"));
            
            
        }catch(Exception e)
        {
            if(navega==1)
                JOptionPane.showMessageDialog(null, "voce ja esta no ultimo registro");
            else if(navega==2)
                JOptionPane.showMessageDialog(null,"voce ja esta no primeiro registro");
            

            
        }
    }

