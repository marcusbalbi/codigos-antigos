    private void bt_gravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_gravarActionPerformed
        try
        {
            if(tf_nome.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "campo nome nao pode ser vazio ");
                return;

            }else if(tf_codigo.getText().length() != 0)
            {
                JOptionPane.showMessageDialog(null,"campo codigo ainda contem valor\n clique em inserir antes de gravar" );
                return;
            }
            else
            {
            con_cidade.executaUpdate("INSERT INTO CIDADE(nome,uf) VALUES('"+tf_nome.getText()+"'"+", "+"'"+cb_uf.getSelectedItem()+"'"+")");
            JOptionPane.showMessageDialog(null, "cidade gravada com Sucesso");
            

            }

            con_cidade.executaSQL("SELECT * FROM CIDADE ORDER BY "+ordenacao);
            con_cidade.result.first();
            mostra_dados();
            preenche_jtable();
             

        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "nao foi possivel gravar dado");
        }

    }//GEN-LAST:event_bt_gravarActionPerformed

    private void bt_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_excluirActionPerformed
        try
        {
            
            int escolha = JOptionPane.showConfirmDialog(null, "excluir cidade "+tf_nome.getText(),"exclusão", JOptionPane.YES_NO_OPTION);

            if(escolha == JOptionPane.YES_OPTION)
            {
                con_cidade.executaSQL("select * from cidade where codigo="+tf_codigo.getText());
            con_cidade.result.first();
                con_cidade.sta.executeUpdate("DELETE FROM CIDADE WHERE codigo ="+tf_codigo.getText());
                JOptionPane.showMessageDialog(null, "cidade excluida com sucesso");
                

            }
            else
           return;

            con_cidade.executaSQL("SELECT * FROM CIDADE ORDER BY "+ordenacao);
            
            con_cidade.result.first();
                
                mostra_dados();
            preenche_jtable();


        
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }



    }//GEN-LAST:event_bt_excluirActionPerformed

